package com.example.dou.recycler_pojo;


public class ZuopingPojo {

    private int image;
    private String num;

    public ZuopingPojo(final int image, final String num) {
        this.image = image;
        this.num = num;
    }

    public int getImage() {
        return image;
    }

    public void setImage(final int image) {
        this.image = image;
    }

    public String getNum() {
        return num;
    }

    public void setNum(final String num) {
        this.num = num;
    }
}
